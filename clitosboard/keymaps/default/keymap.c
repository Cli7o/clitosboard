static void render_animation(void) {
      
        

        const char* IDLE[4] = {
            bongo0,
            bongo1,
            bongo2,
            bongo1
        };
        uint16_t IDLE_frame_sizes[4] = {
            sizeof(bongo0),
            sizeof(bongo1),
            sizeof(bongo2),
            sizeof(bongo1)
        };

        const char* TAP[2] = {
            bongo3,
            bongo4
        };
        uint16_t TAP_frame_sizes[2] = {
            sizeof(bongo3),
            sizeof(bongo4),
        };

        if (timer_elapsed(timer) > FRAME_DURATION) {
            // Set timer to updated time
            timer = timer_read();

            if (animacion == false) {

                current_frame = (current_frame + 1) % (sizeof(IDLE) / sizeof(IDLE[0]));
                oled_write_raw_P(IDLE[current_frame], IDLE_frame_sizes[current_frame]);
            }
            else
            {
                current_frame = (current_frame + 1) % (sizeof(TAP) / sizeof(TAP[0]));
                oled_write_raw_P(TAP[current_frame], TAP_frame_sizes[current_frame]);
            }
        }
}
    

