bool process_record_user(uint16_t keycode, keyrecord_t *record) {

    if (record->event.pressed) {
        animacion = true;
    } else {
        animacion = false;
    }

    if (get_highest_layer(layer_state) == 1) {              
        if (keycode == KC_UP && record->event.pressed) { 
            capa++;
        }
        if (keycode == KC_DOWN && record->event.pressed) {
            capa--;
        }
        /*
        if (keycode == KC_LEFT && record->event.pressed) {
            accion = -1;
        }
        else if (keycode == KC_LEFT && !record->event.pressed) {
            accion = 0;
        }
        if (keycode == KC_RIGHT && record->event.pressed) {
            accion = 1;
        }
        else if (keycode == KC_RIGHT && !record->event.pressed) {
            accion = 0;
        }*/
    }

    if (capa <= capa_minima) {
        capa = capa_minima;
    } else if (capa >= capa_maxima) {
        capa = capa_maxima;
    }
    
    return true; // Permitir que otras acciones de la tecla se procesen

}



